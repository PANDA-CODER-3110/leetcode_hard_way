class Solution
{
  public:
    int findOnce(int arr[], int n)
    {
        int s =0 , e = n-1  ; 
        while(s<=e)
        {
            int m = (s+e)/2 ; 
            if(arr[m]==arr[m^1]) s= m+1 ; 
            else e = m-1 ; 
        }
        return arr[s] ; 
    }
};

